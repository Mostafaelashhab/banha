    /** Five explicit money-page landings + a couple of variants. */
    public function landing(string $slug)
    {
        $spec = self::LANDINGS[$slug] ?? null;
        if (! $spec) {
            throw new NotFoundHttpException();
        }
        // Some slugs redirect to a canonical (e.g. /restaurants-in-banha → /best-restaurants-in-banha)
        if (is_string($spec)) {
            return redirect()->route('seo.landing', ['slug' => $spec], 301);
        }

        $businesses = $this->businessesFor($spec);

        return view('seo.landing', [
            'spec'       => $spec,
            'slug'       => $slug,
            'businesses' => $businesses,
            'area'       => null,
            'canonical'  => url('/' . $slug),
            'crumbs'    => [
                ['name' => 'بنهاوي',  'url' => url('/')],
                ['name' => 'الدليل',  'url' => url('/directory')],
                ['name' => $spec['h1'], 'url' => url('/' . $slug)],
            ],
        ]);
    }

    /**
     * Programmatic pattern — `/{slug}-in-{area-slug}-banha`.
     * E.g. `/cafes-in-el-felal-banha`, `/dentists-in-shareh-farid-nada-banha`.
     *
     * We treat the prefix (`cafes`, `dentists`, …) the same way as the
     * explicit landings, but scope the business list to the requested area
     * and rewrite the title + H1 with the area name.
     */
    public function programmatic(string $slug, string $area)
    {
        // The slug here is just the prefix part (e.g. `cafes`).
        $base = $slug . '-in-banha';
        $spec = self::LANDINGS[$base] ?? null;
        if (! is_array($spec)) {
            throw new NotFoundHttpException();
        }

        // Match the area by its English slug (added in 2026_05_15_050000 migration).
        $areaModel = Area::query()
            ->banha()
            ->where('slug_en', $area)
            ->first();
        if (! $areaModel) {
            throw new NotFoundHttpException();
        }

        // Clone the spec but inject the area context into the strings.
        $spec['h1']    = Str::replace('بنها', $areaModel->name . ' · بنها', $spec['h1']);
        $spec['title'] = Str::replaceFirst('بنها', $areaModel->name . ' · بنها', $spec['title']);
        $spec['intro'] = "في حي {$areaModel->name} ببنها، " . Str::lower($spec['intro']);

        $businesses = $this->businessesFor($spec);

        $canonical = url('/' . $slug . '-in-' . $area . '-banha');

        return view('seo.landing', [
            'spec'       => $spec,
            'slug'       => $slug . '-in-' . $area . '-banha',
            'businesses' => $businesses,
            'area'       => $areaModel,
            'canonical'  => $canonical,
            'crumbs'    => [
                ['name' => 'بنهاوي',          'url' => url('/')],
                ['name' => 'الدليل',           'url' => url('/directory')],
                ['name' => $spec['h1'] ?? '', 'url' => $canonical],
            ],
        ]);
    }

    /** Resolve the business list for a spec. Returns a Collection. */
    private function businessesFor(array $spec)
    {
        $q = Business::query()
            ->where('is_active', true)
            ->when(! empty($spec['category']),  fn ($q) => $q->where('category', $spec['category']))
            ->when(! empty($spec['sub_types']), fn ($q) => $q->whereIn('sub_type', $spec['sub_types']))
            ->when(! empty($spec['filter_24h']), fn ($q) => $q->where('is_24h', true))
            ->with(['zone:id,name', 'photos:id,business_id,url'])
            ->orderByDesc('is_verified')
            ->orderByDesc('rating_avg')
            ->orderByDesc('views_count')
            ->limit(60);

        return $q->get();
    }

    /** Slugs that should appear in sitemap.xml (excluding the redirect aliases). */
    public static function indexableSlugs(): array
    {
        return array_keys(array_filter(self::LANDINGS, 'is_array'));
    }
